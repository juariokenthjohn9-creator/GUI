    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
         int row = jTable1.getSelectedRow();

    if(row < 0){
        javax.swing.JOptionPane.showMessageDialog(null, "Please select a service to delete!");
        return;
    }

    int confirm = javax.swing.JOptionPane.showConfirmDialog(
            null, 
            "Are you sure you want to delete this service?", 
            "Confirm Delete", 
            javax.swing.JOptionPane.YES_NO_OPTION
    );

    if(confirm != javax.swing.JOptionPane.YES_OPTION){
        return;
    }

    try {
        int id = Integer.parseInt(jTable1.getValueAt(row, 0).toString());

        config.conf c = new config.conf();
        java.sql.Connection conn = c.getConnection();

        if(conn == null){
            JOptionPane.showMessageDialog(null, "Database connection failed!");
            return;
        }

        String sql = "DELETE FROM services WHERE id=?";
        java.sql.PreparedStatement ps = conn.prepareStatement(sql);
        ps.setInt(1, id);
        int deleted = ps.executeUpdate();

        if(deleted > 0){
            JOptionPane.showMessageDialog(null, "Service deleted successfully!");
        } else {
            JOptionPane.showMessageDialog(null, "No record found to delete!");
        }

        loadData(); // Refresh table

    } catch(Exception e){
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(null, "Delete failed: " + e.getMessage());
    }
    
    }//GEN-LAST:event_jButton4ActionPerformed

