    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
     try {
        config.conf c = new config.conf();
        String sql = "SELECT * FROM services WHERE service_name LIKE ?";
        java.sql.Connection conn = c.getConnection();
        java.sql.PreparedStatement ps = conn.prepareStatement(sql);
         String keyword = null;
        ps.setString(1, "%" + keyword + "%"); // search keyword
        java.sql.ResultSet rs = ps.executeQuery();

        javax.swing.table.DefaultTableModel model =
                (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // clear table

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("service_name"),
                rs.getDouble("price"),
                rs.getString("description")
            });
        }

        rs.close();
        ps.close();
        conn.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Search failed: " + e.getMessage());
    }    
    }//GEN-LAST:event_jTextField1ActionPerformed

